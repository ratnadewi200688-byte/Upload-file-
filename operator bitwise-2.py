# operator bitwise 

a = 6
b = 3

# bitwise AND (&)
c = a & b
print('nilai a :', a)
print('nilai b :', b)
print('nilai bitwise AND :',c,)

# bitwise or (|)
c = a | b
print('nilai a :', a)
print('nilai b :', b)
print('nilai bitwise OR:',c,)

# bitwise XOR (^)
c = a ^ b
print('nilai a :', a)
print('nilai b :', b)
print('nilai bitwise XOR:',c,)

# bitwise not (~)
c = ~a  
c = ~b
print('nilai a :', a)
print('nilai b :', b)
print('nilai bitwise not:',c,)

# shift right (>>)
c = a >> 2
print('nilai a :', a)
print('nilai b :', 2)
print('nilai shift right:',c,)

# shift left (<<)
c = a << 2
print('nilai a :', a)
print('nilai b :', 2)
print('nilai shift left:',c,)
