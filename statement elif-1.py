# if-elif-else  
makanan = input("Apa makanan favoritmu? ")

if makanan == "nasi goreng":
    print("Nasi goreng memang enak dan mudah di buat !")
elif makanan == "mie ayam":
    print("Mie ayam itu favorit sejuta umat!")
elif makanan == "bakso":
    print("Bakso panas paling cocok dimakan waktu hujan!")
else:
    print(f"{makanan} juga pasti enak kok!")